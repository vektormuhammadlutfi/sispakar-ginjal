						<p style="text-align: justify;">Ginjal merupakan organ penting yang berfungsi menjaga komposisi darah dengan mencegah menumpuknya limbah dan mengendalikan keseimbangan cairan dalam tubuh, menjaga level elektrolit seperti sodium, potasium dan fosfat tetap stabil, serta memproduksi hormon dan enzim yang membantu dalam mengendalikan tekanan darah, membuat sel darah merah dan menjaga tulang tetap kuat.</p>
						<p style="text-align: justify;">Untuk meningkatkan kesadaran akan pentingnya ginjal untuk kesehatan secara menyeluruh dan menurunkan  frekuensi dan dampak penyakit ginjal dan problem kesehatan terkait, diperingati World Kidney Day (WKD) atau Hari Ginjal Sedunia setiap hari Kamis pada minggu kedua di bulan Maret. Peringatan ini dimulai sejak tahun 2006 dan tahun ini Hari Ginjal Sedunia jatuh pada                         tanggal 9 Maret 2017 dengan tema “Penyakit Ginjal dan Obesitas, Gaya Hidup Sehat untuk Ginjal yang Sehat (Kidney disease and obesity, healthy lifestyle for healthy kidneys)”. </p>
						<p style="text-align: justify;">Riskesdas 2013 mengumpulkan data responden yang didiagnosis dokter menderita penyakit gagal ginjal kronis, juga beberapa faktor risiko penyakit ginjal yaitu hipertensi, diabetes melitus dan obesitas.</p>
						<p style="text-align: justify;">Hasil Riskesdas 2013,  populasi umur ≥ 15 tahun yang terdiagnosis gagal ginjal kronis  sebesar 0,2%. Angka ini lebih rendah dibandingkan prevalensi PGK di negara-negara lain, juga hasil penelitian Perhimpunan Nefrologi Indonesia (Pernefri) tahun 2006, yang mendapatkan prevalensi PGK sebesar 12,5%. Hal ini karena Riskesdas 2013 hanya menangkap data orang yang terdiagnosis PGK sedangkan sebagian besar PGK di Indonesia baru terdiagnosis pada tahap lanjut dan akhir.</p>
						<p style="text-align: justify;">Hasil Riskesdas 2013 juga menunjukkan prevalensi meningkat seiring dengan bertambahnya umur, dengan peningkatan tajam pada kelompok umur 35-44 tahun dibandingkan kelompok umur 25-34 tahun. Prevalensi pada laki-laki (0,3%) lebih tinggi dari perempuan (0,2%), prevalensi lebih tinggi terjadi pada masyarakat perdesaan (0,3%), tidak bersekolah (0,4%), pekerjaan wiraswasta, petani/nelayan/buruh (0,3%), dan kuintil indeks kepemilikan terbawah dan menengah bawah masing-masing 0,3%. Sedangkan provinsi dengan prevalensi tertinggi adalah Sulawesi Tengah sebesar 0,5%, diikuti Aceh, Gorontalo, dan Sulawesi Utara masing-masing 0,4 %.</p>
